package com.longlongyu.Info;

public class CateInfo {
	private int cate;   // 分类编号
	private String cate_name; // 分类名
	public int getCateId() {
		return cate;
	}

	public void setCateId(int cate) {
		this.cate = cate;
	}
	
	public String getCateName() {
		return cate_name;
	}

	public void setCateName(String cate_name) {
		this.cate_name = cate_name;
	}
}
